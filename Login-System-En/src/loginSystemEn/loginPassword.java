    void welcome(){
    
      GregorianCalendar time = new GregorianCalendar();
      int hour = time.get(Calendar.HOUR_OF_DAY);
      if(hour<12){
          welcome11.setText("Good Morning!");
      }else if(hour>=12 && hour<=17){
          welcome11.setText("Good Afternoon!");
      }else if(hour>=18 && hour <=20){
           welcome11.setText("Good Evening!");
      }else{
           welcome11.setText("Good Night");
      }


}
