    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String ins_name = ins_name_field.getText();
        String province = province_field.getSelectedItem().toString();
        String district = district_field.getSelectedItem().toString();
        String localLevel = localLevel_field.getText();
        String ward_no=ward_field.getText();
        String local_add=localAddress_field.getText();
        
      
        try { 
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/result_database","root","");
            pst = con1.prepareStatement("insert into institutiondetails(institution_name,province,district,local_level,level_type,ward_no,local_address)values(?,?,?,?,?,?,?)");
            pst.setString(1,ins_name);
            pst.setString(2,province);
            pst.setString(3,district);
            pst.setString(4,localLevel);
            pst.setString(5,levelType);
            pst.setString(6,ward_no);
            pst.setString(7,local_add);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Information Added");
          
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(setup.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(setup.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

