    private void createAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccountActionPerformed

        if (emailField.getText().trim().isEmpty() || usernameField.getText().trim().isEmpty() || passwordField.getText().trim().isEmpty() || cpasswordField.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Input fields cannot be empty.", "Validation Error", JOptionPane.OK_OPTION);
        } else {
            try {
                String email = emailField.getText();
                String username = usernameField.getText();
                String password = passwordField.getText();
                String cpassword = cpasswordField.getText();

                if (password.equals(cpassword)) {
                    String query = "insert into SSMS_loginDetails (email,username,password) values(?,?,?)";
                    con = DriverManager.getConnection("jdbc:mysql://localhost/ssms", "root", "");
                    pst = con.prepareStatement(query);
                    pst.setString(1, email);
                    pst.setString(2, username);
                    pst.setString(3, cpassword);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Your account is successfully created.", "Account Created", JOptionPane.OK_OPTION);
                    sendMail();
                    this.hide();
                    new SSMS_NewAccountVerification(RandomCode).setVisible(true);
                    

                } else {
                    JOptionPane.showMessageDialog(this, "Confirm Password did not matched.", "Input Error", JOptionPane.OK_OPTION);
                }

            } catch (SQLException ex) {
                Logger.getLogger(SSMS_CreateAccount.class.getName()).log(Level.SEVERE, null, ex);
            } catch (AddressException ex) {
                Logger.getLogger(SSMS_CreateAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_createAccountActionPerformed

