    public void sendMail() throws AddressException {
        String usernme = usernameField.getText();
        try {
            Random rand = new Random();
            RandomCode = rand.nextInt(999999);
            String host = "smtp.gmail.com";

            String user = "noreply.sendresponse@gmail.com";
            String pass = "forgotpassword";
            String to = emailField.getText();
            String subject = "Verify Your Account";
            String message = "Dear " + usernme + ", You have recently created a account on SSMS. Use the following code to verify account. Code is " + RandomCode + " Thank You!";
            boolean sessionDebug = false;
            Properties pros = System.getProperties();

            pros.put("mail.smtp.starttls.enable", "true");
            pros.put("mail.smtp.host", "host");
            pros.put("mail.smtp.port", "465");
            pros.put("mail.smtp.socketFactory.port", "465");
            pros.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
            pros.put("mail.smtp.socketFactory.fallback", "false");
            pros.put("mail.smtp.auth", "true");
            pros.put("mail.smtp.starttls.required", "true");
            java.security.Security.addProvider(new com.sun.net.ssl.internal.ssl.Provider());
            Session mailSession = Session.getDefaultInstance(pros, null);
            mailSession.setDebug(sessionDebug);
            Message msg = new MimeMessage(mailSession);
            msg.setFrom(new InternetAddress(user));
            InternetAddress[] address = {new InternetAddress(to)};
            msg.setRecipients(Message.RecipientType.TO, address);
            msg.setSubject(subject);
            msg.setText(message);
            Transport transport = mailSession.getTransport("smtp");
            transport.connect(host, user, pass);
            transport.sendMessage(msg, msg.getAllRecipients());
            transport.close();
            // JOptionPane.showMessageDialog(null, "Code has been send to the email");
        } catch (HeadlessException | MessagingException ex) {

            JOptionPane.showMessageDialog(rootPane, ex);

        }

    }

