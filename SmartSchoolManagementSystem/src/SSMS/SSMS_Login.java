    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed

        if(usernameField.getText().trim().isEmpty() || passwordField.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Input fields cannot be empty.", "Validation Error", JOptionPane.OK_OPTION);
        }else{
            try {
                String username=usernameField.getText();
                String password=passwordField.getText();
                String query="select * from SSMS_loginDetails where username=? and password=?";
                con=DriverManager.getConnection("jdbc:mysql://localhost/ssms","root","");
                pst=con.prepareStatement(query);
                pst.setString(1, username);
                pst.setString(2,password);
                Rs=pst.executeQuery();
                if(Rs.next()){
                    this.hide();
                    new SSMS_Home(username).setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(this, "No account found with entered credintials. Try creating new account.", "Account Not Found", JOptionPane.OK_OPTION);
                    usernameField.setText(" ");
                    passwordField.setText(" ");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(SSMS_Login.class.getName()).log(Level.SEVERE, null, ex);
               
            }
        }
        
        
    }//GEN-LAST:event_loginActionPerformed

