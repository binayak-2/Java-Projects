    private void logInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInBtnActionPerformed

        if (username.getText().trim().isEmpty() || question.getText().trim().isEmpty() || answer.getText().trim().isEmpty() || password.getText().trim().isEmpty() || cpassword.getText().trim().isEmpty()) {
            warningText.setText("Sorry, Input fields cannot be empty!");
        } else {

            String usrname = username.getText();
            String uquestion = question.getText();
            String uanswer = answer.getText();
            String upassword = password.getText();
            String ucpassword = cpassword.getText();
            if (password.getText().equals(cpassword.getText())) {
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    con = DriverManager.getConnection("jdbc:mysql://localhost/songs", "root", "");
                    pst = con.prepareStatement("insert into login (username,password,question,answer)values(?,?,?,?)");
                    pst.setString(1, usrname);
                    pst.setString(2, ucpassword);
                    pst.setString(3, uquestion);
                    pst.setString(4, uanswer);

                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Your account is successfully created");
                    login l = new login();
                    this.hide();
                    l.setVisible(true);

                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(create_account.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(create_account.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                warningText.setText("Confirm password is not correct.");
            }
        }
    }//GEN-LAST:event_logInBtnActionPerformed

